window.translatePage = function (lang) {
    if (!lang) {
        lang = localStorage.getItem('preferredLanguage') || 'ku';
    } else {
        localStorage.setItem('preferredLanguage', lang);
    }

    // Update global state/attributes
    document.documentElement.setAttribute('lang', lang);

    // Update data-translate elements
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        const translation = window.getTranslation(key);

        if (translation) {
            let textNodeFound = false;
            for (let node of el.childNodes) {
                if (node.nodeType === 3 && node.nodeValue.trim() !== '') { // Node.TEXT_NODE
                    node.nodeValue = translation;
                    textNodeFound = true;
                }
            }
            if (!textNodeFound) {
                if (el.children.length === 0) {
                    el.textContent = translation;
                } else {
                    let hasText = false;
                    for (let node of el.childNodes) {
                        if (node.nodeType === 3) {
                            node.nodeValue = translation;
                            hasText = true;
                        }
                    }
                    if (!hasText) {
                        el.appendChild(document.createTextNode(translation));
                    }
                }
            }
        }
    });

    // Update selector-based translations (allows full coverage without HTML bloat)
    for (const [selector, key] of Object.entries(selectorMappings)) {
        const elements = document.querySelectorAll(selector);
        elements.forEach(el => {
            const translation = window.getTranslation(key);
            if (translation) {
                let textNodeFound = false;
                for (let node of el.childNodes) {
                    if (node.nodeType === 3 && node.nodeValue.trim() !== '') { // Node.TEXT_NODE
                        node.nodeValue = translation;
                        textNodeFound = true;
                    }
                }
                if (!textNodeFound) {
                    if (el.children.length === 0) {
                        el.textContent = translation;
                    } else {
                        let hasText = false;
                        for (let node of el.childNodes) {
                            if (node.nodeType === 3) {
                                node.nodeValue = translation;
                                hasText = true;
                            }
                        }
                        if (!hasText) {
                            el.appendChild(document.createTextNode(translation));
                        }
                    }
                }
            }
        });
    }

    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        const translation = window.getTranslation(key);
        if (translation) {
            el.setAttribute('placeholder', translation);
        }
    });

    // Update placeholder-based inputs specifically for login/register forms
    document.querySelectorAll('#loginUsername').forEach(el => el.setAttribute('placeholder', window.getTranslation('auth_ph_username')));
    document.querySelectorAll('#loginPassword').forEach(el => el.setAttribute('placeholder', window.getTranslation('auth_ph_password')));
    document.querySelectorAll('#registerUsername').forEach(el => el.setAttribute('placeholder', window.getTranslation('auth_ph_choose_username')));
    document.querySelectorAll('#registerPassword').forEach(el => el.setAttribute('placeholder', window.getTranslation('auth_ph_create_password')));
    document.querySelectorAll('#registerConfirm').forEach(el => el.setAttribute('placeholder', window.getTranslation('auth_ph_confirm_password')));

    // Update navigation dock labels globally
    document.querySelectorAll('.nav-label').forEach(el => {
        const text = el.textContent.trim().toLowerCase();
        const navKeys = ['nav_profile', 'nav_friends', 'nav_leaderboards', 'nav_store', 'nav_home'];
        let foundKey = '';
        const normalize = s => s ? s.replace(/\s+/g, '').replace(/[\u2000-\u206F\u2E00-\u2E7F\uFE00-\uFE0F\W]/g, '') : '';
        for (const k of navKeys) {
            const en = (window.translations.en && window.translations.en[k]) ? window.translations.en[k].toLowerCase() : '';
            const ku = (window.translations.ku && window.translations.ku[k]) ? window.translations.ku[k].toLowerCase() : '';
            if (!en && !ku) continue;
            if (text === en || text === ku) {
                foundKey = k;
                break;
            }
            // compare normalized (no spaces/punctuation) forms to catch small variants
            if (normalize(text) && (normalize(en) === normalize(text) || normalize(ku) === normalize(text))) {
                foundKey = k;
                break;
            }
        }
        if (foundKey) {
            el.textContent = window.getTranslation(foundKey);
        }
    });

    // Update active state in settings language selector
    const segmentedControl = document.getElementById('languageControl');
    const btnEN = document.getElementById('btnLangEN');
    const btnKU = document.getElementById('btnLangKU');

    if (segmentedControl && btnEN && btnKU) {
        if (lang === 'ku') {
            segmentedControl.classList.add('ku-active');
            btnEN.classList.remove('active');
            btnKU.classList.add('active');
        } else {
            segmentedControl.classList.remove('ku-active');
            btnEN.classList.add('active');
            btnKU.classList.remove('active');
        }
    }
};
